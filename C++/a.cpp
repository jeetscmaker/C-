#include <bits/stdc++.h>
using namespace std;
// contest: Codeforces Round #655 (Div. 2), problem: (A) Omkar and Completion
int main()
{
  int t;
  cin >> t;
  for (int tt = 0; tt < t; ++tt)
  {
     int n;
     cin >> n;
     for (int i = 0; i < n; ++i)
     {
        cout << 1 << " ";
     }
     cout << "\n";
  }
   return 0;
}